package com.smartfacts.mid.api;

import com.smartfacts.mid.api.common.ApiException;
import com.smartfacts.mid.api.common.ApiClient;
import com.smartfacts.mid.api.models.User;
import java.util.*;

public class LoginApi
{
	private ApiClient apiClient;
	
	public LoginApi(ApiClient client)
	{
		this.apiClient = client;
	}
	
	public void setClient(ApiClient client)
	{
		this.apiClient = client;
	}

	public ApiClient getClient()
	{
		return this.apiClient;
	}

	public User login (String email, String password) throws ApiException
	{
		// create path and map variables
		String path = "/login".replaceAll("\\{format\\}","json");

		// query params
		Map<String, String> queryParams = new HashMap<String, String>();
		Map<String, String> headerParams = new HashMap<String, String>();

		// verify required params are set
		if(email == null || password == null )
		{
			throw new ApiException(400, "missing required params");
		}
		try
		{
			String response = apiClient.invokeAPI(path, "POST", queryParams, null, headerParams);
			if (response != null)
			{
				return (User) ApiClient.deserialize(response, "", User.class);
			}
			else
			{
				return null;
			}
		}
		catch (ApiException ex)
		{
			if(ex.getCode() == 404)
			{
				return null;
			}
			else
			{
				throw ex;
			}
		}
	}
	}

